bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  if (record->event.pressed) {
    // ----------- DEFAULT OLED BEHAVIOR ----------- 
    // #ifdef SSD1306OLED
    //     set_keylog(keycode, record);
    // #endif
    // ----------- ------- ---- -------- ----------- 

    // set_timelog();
  }

  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        persistent_default_layer_set(1UL<<_QWERTY);
      }
      return false;
    case NumNav:
      if (record->event.pressed) {
        layer_on(_NumNav);
        // update_tri_layer_RGB(_NumNav, _Symbols, _FMedia);
      } else {
        layer_off(_NumNav);
        // update_tri_layer_RGB(_NumNav, _Symbols, _FMedia);
      }
      return false;
    case Symbols:
      if (record->event.pressed) {
        layer_on(_Symbols);
        // update_tri_layer_RGB(_NumNav, _Symbols, _FMedia);
      } else {
        layer_off(_Symbols);
        // update_tri_layer_RGB(_NumNav, _Symbols, _FMedia);
      }
      return false;
    case FMedia:
      if (record->event.pressed) {
        layer_on(_FMedia);
      } else {
        layer_off(_FMedia);
      }
      return false;
    case KEYMAP:
      if (record->event.pressed) {
        SEND_STRING("https://github.com/antonioxdias/qmk_firmware/tree/master/keyboards/crkbd/keymaps/antonioxdias");
      } else {
        // when keycode is released
      }
      break;
    case LOG:
      if (record->event.pressed) {
        SEND_STRING("console.log()"SS_TAP(X_LEFT));
      } else {
        // when keycode is released
      }
      break;
    case ARROWFUNC:
      if (record->event.pressed) {
        SEND_STRING("=>");
      } else {
        // when keycode is released
      }
      break;
    case LITERAL:
      if (record->event.pressed) {
        SEND_STRING("${}"SS_TAP(X_LEFT));
      } else {
        // when keycode is released
      }
      break;

    }
  return true;
}

